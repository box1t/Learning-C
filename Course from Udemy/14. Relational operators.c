#include <stdio.h>

int main(){
    printf("Output of 4 == 5 relational operator is %d\n\n", 4 == 5);

    int num1 = 10;
    int num2 = 12;

    printf("Here we work with %d and %d", num1, num2);
    printf("\nThe output of %d <= %d is %d", num1, num2, num1 <= num2);
    printf("\nThe output of num1 != num2 is %d\n\n", num1 != num2);

return 0;
}
